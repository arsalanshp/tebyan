public class Folder {
    String folderName;
    String imageUrl;
    boolean isFolder;

    public Folder(String folderName, String imageUrl, boolean isFolder) {
        this.folderName = folderName;
        this.imageUrl = imageUrl;
        this.isFolder = isFolder;
    }

    public String getFolderName() {
        return folderName;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public boolean getIsFolder() {
        return isFolder;
    }
}
